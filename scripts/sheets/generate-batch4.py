# Four more hospitals from their own published maps. Each is built to its
# sheet's design; fidelity is matched to the source:
#   queens.ts   Queen's Hospital, Romford (BHRUT) — site map overlay with the
#               entrances placed exactly, and the tower wards from the sheet's
#               floor schematic grouped by floor at the tower.
#   kgh.ts      King George Hospital (BHRUT) — two real interior floor plans
#               (Ground + First) as a 2-floor venue; every labelled ward and
#               department placed at its room on the plan.
#   broomfield.ts Broomfield Hospital (Mid & South Essex) — the trust's
#               colour-zoned wayfinding map (Zones A-E, Levels 1-4). Key
#               departments placed on their zone; the sheet's code (e.g. A204 =
#               Zone A, Level 2) gives zone + floor.
#   basildon.ts Basildon & Thurrock University Hospital (Mid & South Essex) —
#               the trust's isometric site map; the buildings it labels are
#               placed at their positions.
#
# Positions are read off each sheet; within a building/zone they're
# illustrative (as with the other trust venues). Run:
#   python3 scripts/sheets/generate-batch4.py

