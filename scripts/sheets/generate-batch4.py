FLOOR_LABELS = {-1: "Lower Ground", 0: "Ground Floor", 1: "First Floor", 2: "Second Floor",
                3: "Third Floor", 4: "Fourth Floor", 5: "Fifth Floor"}


def slug(s):
    return re.sub(r"[^a-z0-9]+", "-", s.lower().replace("’", "").replace("'", "")).strip("-") or "wp"


def wtype(name):
    n = name.lower()
    if "entrance" in n:
        return "exit"
    if "pharmac" in n:
        return "pharmacy"
    if "restaurant" in n or "café" in n or "cafe" in n or "coffee" in n or "costa" in n:
        return "canteen"
    if "reception" in n:
        return "reception"
    if "toilet" in n:
        return "toilet"
    if re.search(r"\bward\b|\bunit\b|\bitu\b|\bicu\b|\bhdu\b|suite", n) and "outpatient" not in n and "day unit" not in n:
        return "ward"
    return "department"


def emit(cfg, wps, fname):
    (latS, lngW), (latN, lngE) = cfg["bounds"]
    L = []; A = L.append
    A('import { Venue } from "../types"')
    A("")
    for line in cfg["comment"]:
        A(f"// {line}")
    A("// Generated by scripts/sheets/generate-batch4.py — edit that, not this.")
    A("")
    A(f"export const {cfg['const']}: Venue = {{")
    A(f'  id: "{cfg["id"]}",')
    A(f'  slug: "{cfg["slug"]}",')
    A(f'  name: "{cfg["name"]}",')
    A(f'  subtitle: "{cfg["subtitle"]}",')
    A('  category: "hospital",')
    A(f"  center: {{ lat: {cfg['center'][0]}, lng: {cfg['center'][1]} }},")
    A("  defaultZoom: 17,")
    A('  visibility: "public",')
    A("  verified: false,")
    A(f'  accessibility: {{ stepFreeRoute: true, accessibleToilets: true, notes: "{cfg["access"]}" }},')
    A("  quickAccess: [" + ", ".join(f'"{q}"' for q in cfg["quick"]) + "],")
    A("  floorPlans: [")
    for f in cfg["floors"]:
        img = cfg["plan"] if isinstance(cfg["plan"], str) else cfg["plan"][f]
        A(f'    {{ id: "f{f}", floor: {f}, label: "{FLOOR_LABELS[f]}", imageUrl: "{img}", '
          f"bounds: [[{latS}, {lngW}], [{latN}, {lngE}]] }},")
    A("  ],")
    A("  waypoints: [")
    used = {}; grp = None
    for w in wps:
        if w.get("group") != grp:
            grp = w.get("group")
            if grp:
                A(f"    // ── {grp} " + "─" * max(1, 62 - len(grp)))
        base = f"{slug(w['name'])}-f{w['floor']}"
        n = used.get(base, 0); used[base] = n + 1
        wid = base if n == 0 else f"{base}-{n+1}"
        lat = round(latN - w["ny"] * (latN - latS), 6)
        lng = round(lngW + w["nx"] * (lngE - lngW), 6)
        nm = w["name"].replace('"', '\\"')
        desc = w.get("desc", "")
        dpart = f', description: "{desc}"' if desc else ""
        A(f'    {{ id: "{wid}", name: "{nm}", type: "{w.get("type") or wtype(w["name"])}", '
          f'coordinates: {{ lat: {lat}, lng: {lng} }}, floor: {w["floor"]}{dpart} }},')
    A("  ],")
    A("}")
    A("")
    open(os.path.join(REPO, "src", "lib", "venues", fname), "w").write("\n".join(L))
    print(f"wrote src/lib/venues/{fname} ({len(wps)} waypoints)")


